
package EJ4.Entidades;

public class Espectador {

private Integer nombre;
private Integer edad, dineroDisp;

    public Espectador(Integer nombre, Integer edad, Integer dineroDisp) {
        this.nombre = nombre;
        this.edad = edad;
        this.dineroDisp = dineroDisp;
    }

    public Espectador() {
    }

    public Integer getNombre() {
        return nombre;
    }

    public void setNombre(Integer nombre) {
        this.nombre = nombre;
    }

    public Integer getEdad() {
        return edad;
    }

    public void setEdad(Integer edad) {
        this.edad = edad;
    }

    public Integer getDineroDisp() {
        return dineroDisp;
    }

    public void setDineroDisp(Integer dineroDisp) {
        this.dineroDisp = dineroDisp;
    }














    
}
